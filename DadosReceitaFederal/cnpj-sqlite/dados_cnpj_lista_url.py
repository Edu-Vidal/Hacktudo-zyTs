import requests


page = requests.get(url)    
data = page.text
soup = BeautifulSoup(data)

for link in soup.find_all('a'):
    if str(link.get('href')).endswith('.zip'): 
        cam = link.get('href')
        if cam.startswith('http://http'):
            cam = 'http://' + cam[len('http://http//'):] 
        print(cam)
        
